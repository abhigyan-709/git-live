# check the number is even or not

# type casting but here we change one data type to another data type 
# elif is used to check multiple conditions
# else is used to check the condition which is not true

value = int(input("Please enter the number you want to check: ")) # type casting
if value % 2 == 0:
    print("You entered the even number", value)

else:
    print("Number is odd", value)  

